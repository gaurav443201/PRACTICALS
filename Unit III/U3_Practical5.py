# 5. Program to demonstrate slice operation on string objects.

str1 = input("Enter the string: ")

print("Sliced string is: ", str1[1:4])

# The [1:4] slice operation will slice the string from index 1 to 4. 
# The index starts from 0. So, the output will be the string from index 1 to 3.

